import java.awt.*;

public abstract class Forme{
    protected int x, y;//Coordonn√©es
    protected Color color;

    public Forme(Color color, int x, int y){
	this.color = color;
	this.x = x;
	this.y = y;
    }

    public int getX(){
	return this.x;
    }

    public int getY(){
	return this.y;
    }
    
    public void setX(int x){
	this.x = x;
    }
    
    public void setY(int y){
	this.y = y;
    }
    
    public abstract void dessiner(Graphics g);
}


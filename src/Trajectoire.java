import java.util.ArrayList;

public abstract class Trajectoire{

    protected Point[] debutfin;
    
    public Trajectoire(Point debut, Point fin){
        this.debutfin = new Point[2];
	this.debutfin[0] = debut;
	this.debutfin[1] = fin;
    }

    public abstract ArrayList<Point> calculerPoints();      
}
